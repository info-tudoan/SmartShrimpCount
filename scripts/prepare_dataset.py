"""
Merge labeled shrimp datasets and create a proper train/val split.

Sources:
  data/labeled/          — 65 images from WhatsApp video (large shrimp)
  data/labeled_tomnho/   — 150 images from tomnho video (tiny shrimp)

Output:
  data/dataset/
    images/train/   (80%)
    images/val/     (20%)
    labels/train/
    labels/val/
    data.yaml

Usage:
  python scripts/prepare_dataset.py
  python scripts/prepare_dataset.py --val-ratio 0.2 --seed 42
"""
import argparse
import random
import shutil
from pathlib import Path


def prepare_dataset(
    sources: list,
    out_dir: str = "data/dataset",
    val_ratio: float = 0.2,
    seed: int = 42,
):
    out = Path(out_dir)
    for split in ("train", "val"):
        (out / "images" / split).mkdir(parents=True, exist_ok=True)
        (out / "labels" / split).mkdir(parents=True, exist_ok=True)

    # Collect all (image_path, label_path) pairs
    pairs = []
    for src in sources:
        src = Path(src)
        img_dir = src / "images"
        lbl_dir = src / "labels"
        for img_file in sorted(img_dir.glob("*.*")):
            if img_file.suffix.lower() not in {".jpg", ".jpeg", ".png", ".bmp"}:
                continue
            lbl_file = lbl_dir / (img_file.stem + ".txt")
            if not lbl_file.exists():
                print(f"[SKIP] No label for {img_file.name}")
                continue
            # Check label is non-empty
            if lbl_file.stat().st_size == 0:
                print(f"[SKIP] Empty label: {lbl_file.name}")
                continue
            pairs.append((img_file, lbl_file))

    print(f"Total labeled pairs: {len(pairs)}")

    random.seed(seed)
    random.shuffle(pairs)

    n_val = max(1, int(len(pairs) * val_ratio))
    n_train = len(pairs) - n_val

    val_pairs   = pairs[:n_val]
    train_pairs = pairs[n_val:]

    print(f"  train: {len(train_pairs)}  |  val: {len(val_pairs)}")

    def copy_pairs(pair_list, split):
        for img_src, lbl_src in pair_list:
            # Prefix with source dir name to avoid name collisions
            prefix = img_src.parent.parent.name  # e.g. "labeled" or "labeled_tomnho"
            stem = f"{prefix}_{img_src.stem}"
            shutil.copy2(img_src, out / "images" / split / (stem + img_src.suffix))
            shutil.copy2(lbl_src, out / "labels" / split / (stem + ".txt"))

    copy_pairs(train_pairs, "train")
    copy_pairs(val_pairs, "val")

    # Write data.yaml
    yaml_content = f"""# Merged shrimp dataset — generated by scripts/prepare_dataset.py
path: {out.resolve().as_posix()}
train: images/train
val:   images/val

nc: 1
names: ['shrimp']
"""
    (out / "data.yaml").write_text(yaml_content, encoding="utf-8")
    print(f"\nDataset ready -> {out}/data.yaml")
    print(f"  Train images : {len(train_pairs)}")
    print(f"  Val   images : {len(val_pairs)}")
    return str(out / "data.yaml")


if __name__ == "__main__":
    parser = argparse.ArgumentParser()
    parser.add_argument("--val-ratio", type=float, default=0.2)
    parser.add_argument("--seed", type=int, default=42)
    parser.add_argument("--out", default="data/dataset")
    args = parser.parse_args()

    SOURCES = [
        "data/labeled",
        "data/labeled_tomnho",
    ]
    prepare_dataset(SOURCES, args.out, args.val_ratio, args.seed)
